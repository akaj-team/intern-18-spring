package vn.asiantech.internship.model;

public class BodyDrawer {
    private int icon;
    private int text;

    public BodyDrawer(int icon, int text) {
        this.icon = icon;
        this.text = text;
    }

    public int getIcon() {
        return icon;
    }

    public int getText() {
        return text;
    }
}
